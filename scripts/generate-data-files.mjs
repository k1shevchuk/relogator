import { existsSync, readFileSync, writeFileSync } from "node:fs"
import { mkdir } from "node:fs/promises"
import path from "node:path"
import { fileURLToPath } from "node:url"

const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..")
const outputPath = path.join(rootDir, "src", "domain", "data-files.ts")

function readJson(relativePath) {
  return JSON.parse(readFileSync(path.join(rootDir, relativePath), "utf8"))
}

function assertFile(relativePath) {
  if (!existsSync(path.join(rootDir, relativePath))) {
    throw new Error(`Missing data file: ${relativePath}`)
  }
}

function importName(prefix, countryCode) {
  return `${prefix}${countryCode}`
}

const manifest = readJson("data/catalog.json")
const countryCodes = manifest.currentCountryCodes

if (!Array.isArray(countryCodes) || countryCodes.length === 0) {
  throw new Error("data/catalog.json must define currentCountryCodes")
}

assertFile("data/documents/index.json")
assertFile("data/requirements/index.json")

for (const countryCode of countryCodes) {
  assertFile(`data/countries/${countryCode}.json`)
  assertFile(`data/sources/${countryCode}.json`)
  assertFile(`data/routes/${countryCode}.json`)
}

const countryImports = countryCodes
  .map(
    (countryCode) =>
      `import ${importName("country", countryCode)} from "../../data/countries/${countryCode}.json"`
  )
  .join("\n")
const sourceImports = countryCodes
  .map(
    (countryCode) =>
      `import ${importName("sources", countryCode)} from "../../data/sources/${countryCode}.json"`
  )
  .join("\n")
const routeImports = countryCodes
  .map(
    (countryCode) =>
      `import ${importName("routes", countryCode)} from "../../data/routes/${countryCode}.json"`
  )
  .join("\n")

const countryValues = countryCodes
  .map((countryCode) => importName("country", countryCode))
  .join(",\n    ")
const sourceValues = countryCodes
  .map((countryCode) => `...${importName("sources", countryCode)}`)
  .join(",\n    ")
const routeValues = countryCodes
  .map((countryCode) => `...${importName("routes", countryCode)}`)
  .join(",\n    ")

const generated = `// This file is generated by scripts/generate-data-files.mjs.\n// Do not edit it by hand; update data/*.json and run npm run generate:data-manifest.\n\nimport catalogManifest from "../../data/catalog.json"\nimport documentsData from "../../data/documents/index.json"\nimport requirementsData from "../../data/requirements/index.json"\n${countryImports}\n${sourceImports}\n${routeImports}\n\nexport const rawDataCatalog = {\n  manifest: catalogManifest,\n  countries: [\n    ${countryValues},\n  ],\n  sources: [\n    ${sourceValues},\n  ],\n  requirements: requirementsData,\n  documents: documentsData,\n  routes: [\n    ${routeValues},\n  ],\n}\n`

await mkdir(path.dirname(outputPath), { recursive: true })
writeFileSync(outputPath, generated, "utf8")
console.log(
  `Generated src/domain/data-files.ts for ${countryCodes.length} countries.`
)
